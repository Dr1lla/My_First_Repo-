n = 5  # Висота піраміди

for i in range(1, n + 1):  # Зовнішній цикл по рядках
    for j in range(i, 0, -1):  # Внутрішній цикл по кількості елементів у рядку, числа зменшуються
        print(j, end=" ")  # Виводимо числа в зворотному порядку від i до 1
    print()  # Перехід на новий рядок після завершення кожного рядка
