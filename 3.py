n = 5  # Висота піраміди

for i in range(1, n + 1):  # Зовнішній цикл по рядках
    for j in range(1, i + 1):  # Внутрішній цикл, який збільшується від 1 до i
        print(j, end=" ")  # Виводимо числа від 1 до поточного значення рядка
    print()  # Перехід на новий рядок після завершення кожного рядка
